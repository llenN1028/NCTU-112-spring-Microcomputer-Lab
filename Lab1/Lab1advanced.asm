ORG 0000H
AJMP MAIN
ORG 0050H

MAIN:
	 MOV R0,#01111111B
	 MOV R1,#11111110B
LOOP:
	 MOV A, R0
	 XRL A, R1
	 CPL A
	 MOV P1, A
	 MOV A, R0
	 RR A
	 MOV R0, A
	 MOV A, R1
	 RL A
	 MOV R1, A
	 ACALL DELAY
	 JMP LOOP

DELAY:
	 MOV R5, #00FH
DELAY1:
	 MOV R6, #0FFH
DELAY2:
 	 MOV R7,  #0FFH
DELAY3:
	 DJNZ R7, DELAY3
	 DJNZ R6, DELAY2
	 DJNZ R5, DELAY1
	 RET