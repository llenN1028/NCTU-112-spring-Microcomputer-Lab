ORG 0000H
AJMP MAIN
ORG 0050H

MAIN:
     MOV A, #7FH
LOOP1:
	 MOV P1, A
	 MOV R0, #00000011B
	 MOV P0, R0
	 RL A
	 ACALL  DELAY	 
LOOP2:
	 MOV P1, A
	 MOV R0, #00000110B
	 MOV P0, R0
	 RL A
	 ACALL DELAY
	 JMP LOOP1
DELAY:
	 MOV R5, #00FH
DELAY1:
	 MOV R6, #0FFH
DELAY2:
 	 MOV R7,  #0FFH
DELAY3:
	 DJNZ R7, DELAY3
	 DJNZ R6, DELAY2
	 DJNZ R5, DELAY1
	 RET