ORG 0000H
AJMP START
ORG 0050H

START:
	MOV R0, #0
	MOV R1, #0
	MOV A, #11001100B
LOOP:
	MOV P0, A
	RR A
	INC R0
	ACALL DELAY
	CJNE R0, #126, LOOP
	MOV A, #10001000B
LOOP1:
	MOV P0, A
	RL A
	INC R1
	ACALL DELAY
	CJNE R1, #252, LOOP1
	AJMP $
DELAY:
	MOV R6, #255
DELAY1:
	MOV R7, #255
DELAY2:
	DJNZ R7, DELAY2
	DJNZ R6, DELAY1
	RET
	END