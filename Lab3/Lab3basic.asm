	DB 080H ;8
	DB 0F8H ;7
	DB 082H ;6
    	DB 092H ;5
	DB 099H ;4
	DB 0B0H ;3
	DB 0A4H ;2
	DB 0F9H ;1
	DB 0C0H ;0

MAIN:
	MOV DPTR, #TABLE
LOOP0:
	MOV R0, #0 
LOOP1:
	MOV A, R0
	MOVC A, @A + DPTR	 
	MOV P1, A
	INC R0
	ACALL DELAY
	CJNE R0 ,#10 ,LOOP1
	AJMP LOOP0

DELAY:
	MOV R5, #055H
DELAY1:
	MOV R6, #0AFH
DELAY2:
	MOV R7, #0FFH
DELAY3:
	DJNZ R7, DELAY3
	DJNZ R6, DELAY2
	DJNZ R5, DELAY1
	RET
	END
	
	
	

	
	
	
	
